#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    cout << "== ABS ==" << endl;
    cout << "Abs of -10:" << abs(-10) << endl;
    cout << "Abs of -5.72:" << fabs(-5.72) << endl;

    cout << "== POW ==" << endl;
    cout << "2 power 2: " << pow(2,2) << endl;
    cout << "3 power 4: " << pow(3,4) << endl;
    cout << "4 power 6: " << pow(4,6) << endl;
    cout << "8 power 3: " << pow(8,3) << endl;

    return 0;
}
